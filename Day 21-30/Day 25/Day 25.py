"""
TASK 1
Create a program that determines how quickly an object is traveling when it hits the
ground. The user will enter the height from which the object is dropped in meters (m).
Because the object is dropped its initial speed is 0m/s. Assume that the acceleration
due to gravity is 9.8m/s2. You can use the formula (see diagram) to compute the
final speed, vf , when the initial speed, vi, acceleration, a, and distance, d, are known.
"""

from math import sqrt

height = float(input("Enter the height from which the object was dropped: "))

#formula:
#final speed = square root of (initial speed^2 + 2ad)

#assuming initial speed of 0 and acceleration of 9.8m/s2
final_speed = sqrt(2*9.8*height)

#printing output
print(f"\n{final_speed:.2f}m/s")

