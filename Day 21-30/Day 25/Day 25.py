#defining function to determine an integer
def isInteger(entry):
  #ensuring that integers with symbols are accommodated
  if entry[0] == "+" or entry[0] == "-" or entry[0].isnumeric():
    
    if entry.isnumeric():   #integers without any preceding symbols
      return "Yes, it is an integer"
    elif entry[1:].isnumeric():   #integers with preceding symbols
      return "Yes, it is an integer"
    else:   #non-integers
      return "No, it is not an integer"

